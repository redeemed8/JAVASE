package MathApi;
import java.lang.Math;
public class Test {
    public static void main(String[] args) {
        System.out.println(Math.pow(2,2));                  //x的y次方

        System.out.println(Math.max(5,9));                  //取最大值
        System.out.println(Math.min(5,9));

        System.out.println(Math.ceil(5.26));                //向上取整

        System.out.println(Math.floor(5.26));               //向下取整

        System.out.println(Math.round(5.624));              //四舍五入

        System.out.println(Math.sqrt(5.0));                 //开平方

        System.out.println(Math.cbrt(27));                  //开立方
    }
}
